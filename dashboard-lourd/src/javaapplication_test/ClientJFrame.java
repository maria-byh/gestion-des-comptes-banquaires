    private void retirerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retirerButtonActionPerformed

        int num_compte_ = Integer.parseInt(comptesComboBox1.getSelectedItem().toString());
        int num_employe_ = Character.getNumericValue(comptesComboBox3.getSelectedItem().toString().charAt(0));
        double montant_ = Double.valueOf(montantTextBox1.getText());

        TOperations to = new TOperations();
        to.setTypeOp("retrait");

        // current date;
        GregorianCalendar gregorianCalendar = new GregorianCalendar();
        DatatypeFactory datatypeFactory;
        try {
            datatypeFactory = DatatypeFactory.newInstance();
            XMLGregorianCalendar xmlGregorianCalendar = datatypeFactory.newXMLGregorianCalendar(gregorianCalendar);
            to.setDateOp(xmlGregorianCalendar);
        } catch (DatatypeConfigurationException ex) {
        }

        to.setMontant(montant_);
        to.setNumEmp(findEmployeById(num_employe_));
        to.setNumCpte(findCompteById(num_compte_));

        retrait(num_compte_, montant_);

        createOperation(to);
        upadteData();
        montantTextBox1.setText("");
    }//GEN-LAST:event_retirerButtonActionPerformed

