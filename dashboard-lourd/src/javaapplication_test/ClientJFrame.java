    public void updateData() {
        List<TComptes> tcs = findAll();

        

        for (TComptes tc : tcs) {
            Object[] rowData = {
                tc.getNumCpte(),
                tc.getTypePte(),
                tc.getDateCr(),
                tc.getSolde() + " DA",
                tc.getNumEmp().getNumEmp() + ". " + tc.getNumEmp().getNomEmp(),
                tc.getCodeCli().getCodeCli() + ". " + tc.getCodeCli().getNomCli()
            };
            dataModel.addRow(rowData);
        }
        DefaultTableModel dataModel = null;

        tablesComptes1.setModel(dataModel);
        tablesComptes.setModel(dataModel);

        DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>();
        for (TComptes tc : tcs) {
            comboBoxModel.addElement(tc.getNumCpte().toString());
        }
        comptesComboBox.setModel(comboBoxModel);
        comptesComboBox1.setModel(comboBoxModel);

        List<TEmployes> tes = findAllEmploye();
        DefaultComboBoxModel<String> comboBoxModel2 = new DefaultComboBoxModel<>();
        for (TEmployes te : tes) {
            comboBoxModel2.addElement(te.getNumEmp() + ". " + te.getNomEmp());
        }
        comptesComboBox2.setModel(comboBoxModel2);
        comptesComboBox3.setModel(comboBoxModel2);
    }

